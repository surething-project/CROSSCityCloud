fr_size <- length(fr)

p <- fr/sum(fr)

lzipf <- function(s,N) -s*log(1:N)-log(sum(1/(1:N)^s))

opt.f <- function(s) sum((log(p)-lzipf(s,length(p)))^2)

# sum of squares fit
opt <- optimize(opt.f,c(0.5,fr_size))

